package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int f = 0;
        while(f < 5){
            int s = 0;
            while(s < 10){
                System.out.print("Q");
                s++;
            }
            System.out.println();
            f++;
        }
    }
}